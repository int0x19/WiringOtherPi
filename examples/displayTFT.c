void set_CS(int status)
{
#ifdef FAST_DATA 
    uint32_t regval=0;
    uint32_t phyaddr = 0;
// ----------------------------------------------------------------------------- 
// CS set/reset
// -----------------------------------------------------------------------------    
    phyaddr = SUNXI_GPIO_BASE + 0x10; // +0x10 -> data reg A
    regval = readl1(phyaddr);
    if (status == LOW)
      regval &= 0xFFFFFFBF;
    else
      regval |= 0x40;
    writel1(regval, phyaddr);
 
#else 
   digitalWrite(CSX, status); 
#endif    
}



