void init() {
int x;
    wiringPiSetup () ;

    pinMode (29, OUTPUT);  //led  
  
    //# prepare all pins for output
    for (x=0;x<12;x++)
    { 
        pinMode (pins[x], OUTPUT);   
        digitalWrite(pins[x], HIGH);
    }

 
  
    //# hard reset sequence
    digitalWrite(RES, HIGH);
    delay(500); //  ## 5 ms
    digitalWrite(RES, LOW);  
    delay(1000); //  ## 15 ms
    digitalWrite(RES, HIGH);    
    delay(150); //  ## 15 ms

                 
    digitalWrite(CSX, HIGH);      
    digitalWrite(WRX, HIGH);     
    digitalWrite(CSX, LOW);       
}



