void StartWrite(unsigned char byte)
{
    #ifdef FAST_DATA 
    uint32_t regval=0;
    uint32_t phyaddr = 0;
// ----------------------------------------------------------------------------- 
// DC set/reset
// -----------------------------------------------------------------------------    
    phyaddr = SUNXI_GPIO_BASE + (3 * 36)+ 0x10; // +0x10 -> data reg D
    regval = readl1(phyaddr);
    regval &= 0xFFFFBFFF;             //DC low
    writel1(regval, phyaddr);
    
    send_data (byte);
    
    regval = readl1(phyaddr);    
    regval |= 0x4000;            //DC high
    writel1(regval, phyaddr);    
  
#else
        cmd(byte);
#endif
}



